import UIKit

class LiarsDiceColors
{
    static func michiganBlue() -> UIColor
    {
        return UIColor(red: 0.0, green: 39.0/255.0, blue: 76.0/255.0, alpha: 1.0)
    }

    static func michiganSelectionBlue() -> UIColor
    {
        return UIColor(red: 0.0, green: 65.0/255.0, blue: 140/255.0, alpha: 1.0)
    }

    static func michiganMaize() -> UIColor
    {
        return UIColor(red: 255.0/255.0, green: 203.0/255.0, blue: 5.0/255.0, alpha: 1.0)
    }
}
